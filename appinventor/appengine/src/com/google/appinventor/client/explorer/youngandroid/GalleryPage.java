/**
 * The gallery list shows apps from the gallery in a table.
 *
 * <p> The project name and date created will be shown in the table.
 *
 * @author wolberd@google.com (Dave Wolber)
 */
public class GalleryPage extends Composite {
  

  GalleryApp app = null;

  /**
   * Creates a new GalleryPage
   */
  public GalleryPage(GalleryApp app) {

    this.app=app;
    // Initialize UI
    VerticalPanel panel = new VerticalPanel();
    panel.setWidth("100%");
    Label nameLabel = new Label(app.getTitle());
    panel.add(nameLabel);
    initWidget(panel);
  }
 
}	  
  
  

 
